int	ft_sqrt(int nb)
{
	long long	r;

	r = 1;
	if (nb <= 0)
		return (0);
	while (r * r <= nb)
	{
		if (nb % r == 0)
		{
			if (r * r == nb)
				return (r);
		}
		r++;
	}
	return (0);
}
