#include <unistd.h>

int	main(int n, char **str)
{
	int	idx;
	int	i;

	idx = 1;
	i = 0;
	while (idx < n)
	{
		i = 0;
		while (str[idx][i] != '\0')
		{
			write(1, &str[idx][i], 1);
			i++;
		}
		write(1, "\n", 1);
		idx++;
	}
}
