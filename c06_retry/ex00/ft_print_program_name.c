#include <unistd.h>

int	main(int n, char **str)
{
	int	idx;

	idx = 0;
	n = 0;
	while (str[0][idx] != '\0')
	{
		write(1, &str[0][idx], 1);
		idx++;
	}
	write(1, "\n", 1);
}
