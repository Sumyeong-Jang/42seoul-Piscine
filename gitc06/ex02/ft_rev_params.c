#include <unistd.h>

int	main(int n, char **str)
{
	int	i;

	i = 0;
	n -= 1;
	while (n > 0)
	{
		i = 0;
		while (str[n][i] != '\0')
		{
			write(1, &str[n][i], 1);
			i++;
		}
		write(1, "\n", 1);
		n--;
	}
}
